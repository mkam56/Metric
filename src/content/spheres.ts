export type SphereId =
  | 'black-hole'
  | 'quasar'
  | 'kerr-black-hole'
  | 'pulsar'
  | 'neutron-star';

export type Vector3Like = {
  x: number;
  y: number;
  z: number;
};

export type CameraPreset = {
  position: Vector3Like;
  target: Vector3Like;
  durationMs?: number;
  arcHeight?: number;
};

export type TourOrbit = {
  enabled: boolean;
  yawAmplitude: number;
  yawSpeed: number;
  verticalBob?: number;
};

export type TourStop = {
  id: string;
  title: string;
  text: string;
  cameraPosition: Vector3Like;
  target: Vector3Like;
  durationMs: number;
  orbit?: TourOrbit;
};

export type SphereDefinition = {
  id: SphereId;
  title: string;
  menuTitle: string;
  eyebrow: string;
  description: string;
  imageUrl: string;
  imagePosition?: string;
  imageScale?: number;
  accentColor: string;
  freeModeIntro: CameraPreset;
  tourStops: TourStop[];
};

const TOUR_STOP_MS = 60_000;

const orbit = (
  yawAmplitude: number,
  yawSpeed: number,
  verticalBob: number
): TourOrbit => ({
  enabled: true,
  yawAmplitude,
  yawSpeed,
  verticalBob,
});

const stop = (
  id: string,
  title: string,
  text: string,
  cameraPosition: Vector3Like,
  target: Vector3Like,
  orbitConfig: TourOrbit
): TourStop => ({
  id,
  title,
  text,
  cameraPosition,
  target,
  durationMs: TOUR_STOP_MS,
  orbit: orbitConfig,
});

const assetUrl = (assetPath: string): string =>
  new URL(assetPath, import.meta.url).href;

